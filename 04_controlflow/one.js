// == operator used to check two things are equal or not
if (2 == 2) {
  console.log("hi");
}

// === operator used to check two things with value and with type are equal or not
if (2 === "2") {
  console.log("equal");
} else {
  console.log("not equal");
}

// use to strict type checking with values
if(2 !== "2"){
    console.log("hi");
}
else{
    console.log("by");
}